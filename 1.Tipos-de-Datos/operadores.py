
#! valores booleanos sirven para true o false
print(type(False))
print(type(True))
#! operadores aritmeticos
#* == doble igual para ver si dos nÃºmero son iguales
print(2 == 2)
print("hello" == "bye")
#* != sirve para ver si dos valores son diferentes
print(3 != 5)
print(3 != 3)
#* > mayor que el valor de la derecha, < menor que el valor de la derecha
print(2 < 3)
print(2 > 1)
#* >= mayor o igual que el valor de la derecha, <= menor o igual que el valor de la derecha
print(12 >= 12)
print(12 <= 11)
print(12 >= 11)
#! operadores logicos 
#* and sirve para ver si ambos elementos son iguales
print(1 < 2 and 2 < 3)
print("y" == "e" and 2 == 2)
#* or, sirve para ver si uno u otro de los valores son iguales
print("y" == "e" or 2 == 2)
#* not, sirve para ver si algo no es algo 
print( not 1 == 2)